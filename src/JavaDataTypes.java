public class JavaDataTypes {
    public static void main(String[] args) {
        // Java Variables
        // Kiểu dữ liệu primitive - Kiểu dữ liệu nguyên thủy
        //int - Giá trị mặc định: o
        int a = 10;
        System.out.println(a);
        //float - Giá trị mặc định: o.of
        float b = 20.5F;
        System.out.println(b);
        //double - Giá trị mặc định: o.od
        double c = 15.2D; // 15.2
        System.out.println(c);
        //char - Giá trị mặc định: '\u0000'
        char d = 'd';
        System.out.println(d);
        //boolean - Giá trị mặc định: false
        boolean e = true;
        System.out.println(e);
        //long - Giá trị mặc định: oL
        long l = 10009999; // 1000099999L
        System.out.println(l);

        // Kiểu dữ liệu non-primitive
        /*
        * Classes
        * Interfaces
        * Objects
        * Strings
        * Arrays
        * */
    }
}
